namespace Math{
    //--------------------------------------------------------------------------
    Vector2D QBezier::eval( const double & t ) const{
        const double s = 1 - t;
        //b(t) = s^2*p0 + 2*s*t*p1 + t^2*p2
        return (p0*(s*s)) + (p1*(2*s*t)) + (p2*(t*t));
    }
}

