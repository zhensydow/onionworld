//------------------------------------------------------------------------------
#include <cstdlib>
#include <iostream>

#include "math/QBezier.h"

//------------------------------------------------------------------------------
int main(int argc, char **argv ){
    Math::QBezier qb{{0,0},{10,10},{20,0}};

    for( int i = 0 ; i < 101 ; ++i ){
        auto v = qb.eval(i / 100.0);
        std::cout << i << ": " << v.x << "," << v.y << std::endl;
    }

    return EXIT_SUCCESS;
}

